#Operators in R
#Assignment Operator-Used to assign values
#1 method
a=7
print(a)
#2 method
b<-7
print(b)
#3 method
TRUE->c
print(c)
#Arithmetic Operator-Used to do simple mathematical functions
#Addition
num1=10
num2=20
print(num1+num2)
#Subtraction
print(num1-num2)
#Multiplication
print(num1*num2)
#Division
print(num1/num2)
#Relational Operator-Used to compare values or find relation between values
print(num1>num2)
print(num1<num2)
print(num1==num2)
print(num1!=num2)
#Logical Operator-Used to compare values & and |
log1=TRUE
log2=FALSE
print(log1&log1)
print(log1&log2)
print(log2&log2)
print(log1|log1)
print(log1|log2)
print(log2|log2)